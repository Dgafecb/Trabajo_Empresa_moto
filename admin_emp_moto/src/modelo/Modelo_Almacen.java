
package modelo;


public class Modelo_Almacen {

    private String id;
    private String nombre_prod;
    private String tipo_vehiculo;
    private String marca;
    private String modelo;
    private String color;
    private String motor;
    private String chasis;
    private String anho_fab;
    private String anhos_garantia;
    private int cantidad;
    private float precio;
    private int limite_advertencia;
    private float precio_compra;
    private int comision;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNombre_prod() {
        return nombre_prod;
    }

    public void setNombre_prod(String nombre_prod) {
        this.nombre_prod = nombre_prod;
    }

    public String getTipo_vehiculo() {
        return tipo_vehiculo;
    }

    public void setTipo_vehiculo(String tipo_vehiculo) {
        this.tipo_vehiculo = tipo_vehiculo;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getMotor() {
        return motor;
    }

    public void setMotor(String motor) {
        this.motor = motor;
    }

    public String getChasis() {
        return chasis;
    }

    public void setChasis(String chasis) {
        this.chasis = chasis;
    }

    public String getAnho_fab() {
        return anho_fab;
    }

    public void setAnho_fab(String anho_fab) {
        this.anho_fab = anho_fab;
    }

    public String getAnhos_garantia() {
        return anhos_garantia;
    }

    public void setAnhos_garantia(String anhos_garantia) {
        this.anhos_garantia = anhos_garantia;
    }

    public int getCantidad() {
        return cantidad;
    }

    public void setCantidad(int cantidad) {
        this.cantidad = cantidad;
    }

    public float getPrecio() {
        return precio;
    }

    public void setPrecio(float precio) {
        this.precio = precio;
    }

    public int getLimite_advertencia() {
        return limite_advertencia;
    }

    public void setLimite_advertencia(int limite_advertencia) {
        this.limite_advertencia = limite_advertencia;
    }

    public float getPrecio_compra() {
        return precio_compra;
    }

    public void setPrecio_compra(float precio_compra) {
        this.precio_compra = precio_compra;
    }

    public int getComision() {
        return comision;
    }

    public void setComision(int comision) {
        this.comision = comision;
    }

    
}
